package com.aizi.xiaohuhu.utility;

public class SleepyInfo {
    
    public float sober;
    public float fallsleep;
    public float lightsleep;
    public float deepsleep;
    
    
    public SleepyInfo(float sober, float fallsleep, 
            float lightsleep, float deepsleep){
        this.sober = sober;
        this.fallsleep = fallsleep;
        this.lightsleep = lightsleep;
        this.deepsleep = deepsleep;
        
    }

}
